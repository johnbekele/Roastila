// Search and filter functions
export const searchCoffees = (coffees, query) => {
  if (!query) return coffees;

  const lowercaseQuery = query.toLowerCase();
  return coffees.filter(
    (coffee) =>
      coffee.name.toLowerCase().includes(lowercaseQuery) ||
      coffee.origin.toLowerCase().includes(lowercaseQuery) ||
      coffee.region.toLowerCase().includes(lowercaseQuery) ||
      coffee.processing.toLowerCase().includes(lowercaseQuery) ||
      coffee.flavorNotes.some((note) =>
        note.toLowerCase().includes(lowercaseQuery)
      ) ||
      coffee.producer.toLowerCase().includes(lowercaseQuery) ||
      coffee.certification.toLowerCase().includes(lowercaseQuery)
  );
};

export const filterCoffees = (coffees, filters) => {
  let filtered = [...coffees];

  if (filters.region) {
    filtered = filtered.filter((coffee) => coffee.region === filters.region);
  }

  if (filters.processing) {
    filtered = filtered.filter(
      (coffee) => coffee.processing === filters.processing
    );
  }

  if (filters.certification) {
    filtered = filtered.filter((coffee) =>
      coffee.certification
        .toLowerCase()
        .includes(filters.certification.toLowerCase())
    );
  }

  if (filters.minRating) {
    filtered = filtered.filter((coffee) => coffee.rating >= filters.minRating);
  }

  if (filters.maxPrice) {
    filtered = filtered.filter(
      (coffee) => parseFloat(coffee.price.replace("€", "")) <= filters.maxPrice
    );
  }

  if (filters.availability) {
    filtered = filtered.filter(
      (coffee) => coffee.availability === filters.availability
    );
  }

  return filtered;
};

// Sort functions
export const sortCoffees = (coffees, sortBy) => {
  const sorted = [...coffees];

  switch (sortBy) {
    case "price-low":
      return sorted.sort(
        (a, b) =>
          parseFloat(a.price.replace("€", "")) -
          parseFloat(b.price.replace("€", ""))
      );
    case "price-high":
      return sorted.sort(
        (a, b) =>
          parseFloat(b.price.replace("€", "")) -
          parseFloat(a.price.replace("€", ""))
      );
    case "rating":
      return sorted.sort((a, b) => b.rating - a.rating);
    case "name":
      return sorted.sort((a, b) => a.name.localeCompare(b.name));
    case "newest":
      return sorted.sort((a, b) => b.harvestYear - a.harvestYear);
    default:
      return sorted;
  }
};
